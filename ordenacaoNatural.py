print("1 - Ordenação Natural")

#Ordenação natural
frutas = ["Laranja", "Banana", "Maça", "Mamão"]
print(frutas)

print("------------------------")
#Ordenação Customizada, random
print("2- Ordenação customizada a partir do alfabeto")


frutas_sorteadas = ["Laranja", "Banana", "Maça", "Mamão"]
frutas_sorteadas.sort()
print(frutas_sorteadas)

print("------------------------")
#Ordem inversa das frutas
print("3- Ordem inversa das frutas")

frutas_sorteadas_inverso = ["Laranja", "Banana", "Maça", "Mamão"]
frutas_sorteadas_inverso.sort(reverse= True)
print(frutas_sorteadas_inverso) #ordem inversa





